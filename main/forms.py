class CarrierCalculatorForm(forms.Form):
    metal_type = forms.ChoiceField(
        label="Implant Metal",
        choices=[(key, key) for key in METAL_TO_ION_DATA.keys()],
        widget=forms.Select(attrs={'class': 'form-select', 'id': 'metal-select'})
    )
    
    ion_energy = forms.ChoiceField(
        label="Ion Energy",
        choices=[],  # Dynamically filled
        widget=forms.Select(attrs={'class': 'form-select', 'id': 'energy-select'})
    )
    
    nb = forms.FloatField(
        label=mark_safe('Substrate Concentration (cm⁻³)'),
        initial=1e15,
        widget=forms.NumberInput(attrs={'class': 'form-control'})
    )
    
    q = forms.FloatField(
        label='Ion Dose Charge (cm⁻²)',
        initial=1e12,
        widget=forms.NumberInput(attrs={'class': 'form-control'})
    )
    
    tox = forms.FloatField(
        label='Oxide Thickness (cm)',
        initial=0.5e-5,
        widget=forms.NumberInput(attrs={'class': 'form-control'})
    )

    anneal_time_s = forms.FloatField(
        label='Anneal Time (seconds)',
        initial=50,
        min_value=1,
        max_value=7200,  # 2 hours maximum
        help_text='Range: 1 to 7200 seconds (2 hours)',
        widget=forms.NumberInput(attrs={
            'class': 'form-control',
            'step': '1',
        })
    )
    
    # Only temperature field for annealing - time is fixed
    anneal_temp_c = forms.FloatField(
        label=mark_safe('Anneal Temperature (°C)'),
        initial=1000,
        min_value=700,
        max_value=1200,
        help_text='Range: 700°C to 1200°C',
        widget=forms.NumberInput(attrs={
            'class': 'form-control',
            'step': '50',
        })
    )

    def __init__(self, *args, **kwargs):
        super().__init__(*args, **kwargs)
        metal = self.data.get('metal_type') or self.initial.get('metal_type') or 'Arsenic'
        ion_data = METAL_TO_ION_DATA.get(metal, ARSENIC_ION_ENERGY_DATA)
        self.fields['ion_energy'].choices = [(key, key) for key in ion_data.keys()]

